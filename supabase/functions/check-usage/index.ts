import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.57.2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const FREE_DAILY_LIMIT = 10;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  const supabaseClient = createClient(
    Deno.env.get("SUPABASE_URL") ?? "",
    Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? "",
    { auth: { persistSession: false } }
  );

  try {
    const authHeader = req.headers.get("Authorization");
    if (!authHeader) throw new Error("No authorization header");

    const token = authHeader.replace("Bearer ", "");
    const { data: userData, error: userError } = await supabaseClient.auth.getUser(token);
    
    if (userError) throw new Error(`Auth error: ${userError.message}`);
    const user = userData.user;
    if (!user) throw new Error("User not authenticated");

    const today = new Date().toISOString().split('T')[0];

    // Get today's usage
    const { data: usageData, error: usageError } = await supabaseClient
      .from('usage_tracking')
      .select('query_count')
      .eq('user_id', user.id)
      .eq('date', today)
      .single();

    const currentCount = usageData?.query_count || 0;
    const remaining = Math.max(0, FREE_DAILY_LIMIT - currentCount);

    console.log("[CHECK-USAGE] User:", user.id, "Count:", currentCount, "Remaining:", remaining);

    return new Response(JSON.stringify({
      current_count: currentCount,
      daily_limit: FREE_DAILY_LIMIT,
      remaining,
      can_query: remaining > 0,
    }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 200,
    });
  } catch (error) {
    const errorMessage = error instanceof Error ? error.message : "Error desconocido";
    console.error("[CHECK-USAGE] Error:", errorMessage);
    return new Response(JSON.stringify({ error: errorMessage }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 500,
    });
  }
});
